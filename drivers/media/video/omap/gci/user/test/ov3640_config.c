int
ov3640_reset()
{
	int ret = 0;
	ret = issue_sensortrans_reg16_data16(sensor_reset,SENSOR_RESET_SIZE);
	return ret;
}

int
ov3640_initial_config()
{
	int ret = 0;
	ret = issue_sensortrans_reg16_data16(sensor_init,SENSOR_INIT_SIZE);
	ret = issue_sensortrans_reg16_data16(sensor_init_xga,
					SENSOR_INIT_XGA_SIZE);
	ret = issue_sensortrans_reg16_data16(sensor_init_xga_yuv,
					SENSOR_INIT_XGA_YUV_SIZE);
	return ret;
}

int
ov3640_preview_config(unsigned int width, unsigned height,
			unsigned int left, unsigned int top)
{
	int ret = 0;

	if((width == CIF_WIDTH) && (height == CIF_HEIGHT)){
		ret = issue_sensortrans_reg16_data16(
					sensor_size_config_CIF_15,
					SENSOR_SIZE_CONFIG_SIZE);
	}
	else if((width == QCIF_WIDTH) && (height == QCIF_HEIGHT)){
		ret = issue_sensortrans_reg16_data16(
					sensor_size_config_QCIF_15,
					SENSOR_SIZE_CONFIG_SIZE);
	}
	else if((width == VGA_WIDTH) && (height == VGA_HEIGHT)){
		ret = issue_sensortrans_reg16_data16(
					sensor_size_config_VGA_15,
					SENSOR_SIZE_CONFIG_SIZE);
	}
	else if((width == QXGA_WIDTH) && (height == QXGA_HEIGHT)){
		ret = issue_sensortrans_reg16_data16(
					sensor_size_config_QXGA_15,
					SENSOR_SIZE_QXGA_CONFIG_SIZE);

	}
	return ret;
}

void 
get_ov3640_resln(unsigned int width, unsigned int height,
				unsigned int* set_width,
				unsigned int* set_height)
{
	if (width <= QCIF_WIDTH) {
		*set_width = QCIF_WIDTH;
		*set_height = QCIF_HEIGHT;
	} else if (width <= CIF_WIDTH) {
		*set_width = CIF_WIDTH;
		*set_height = CIF_HEIGHT;
	} else if (width <= VGA_WIDTH) {
		*set_width = VGA_WIDTH;
		*set_height = VGA_HEIGHT;
	} else if(width <= QXGA_WIDTH){ /* For Burst Mode */
		*set_width =  QXGA_WIDTH;
		*set_height = QXGA_HEIGHT;
	}
	return;
}

